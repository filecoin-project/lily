package generator

import (
	"bytes"
	"fmt"
	"io/ioutil"
	"os"
	"path/filepath"
	"text/template"
)

var latestVersion = 7

var versions = []int{0, 2, 3, 4, 5, 6, latestVersion}

var versionImports = map[int]string{
	0:             "/",
	2:             "/v2/",
	3:             "/v3/",
	4:             "/v4/",
	5:             "/v5/",
	6:             "/v6/",
	latestVersion: "/v7/",
}

var actors = map[string][]int{
	"account":  versions,
	"cron":     versions,
	"init":     versions,
	"market":   versions,
	"miner":    versions,
	"multisig": versions,
	"paych":    versions,
	"power":    versions,
	"reward":   versions,
	"verifreg": versions,
}

func Gen() error {
	if err := generateAdapters(); err != nil {
		return err
	}

	if err := generatePolicy(); err != nil {
		return err
	}

	if err := generateBuiltin(); err != nil {
		return err
	}
	return nil
}

func generateAdapters() error {
	actorsDir := "chain/actors/builtin"
	for act, versions := range actors {
		actDir := filepath.Join(actorsDir, act)

		if err := generateState(actDir); err != nil {
			return err
		}

		if err := generateMessages(actDir); err != nil {
			return err
		}

		{
			af, err := ioutil.ReadFile(filepath.Join(actDir, "actor.go.template"))
			if err != nil {
				return fmt.Errorf("loading actor template: %w", err)
			}

			tpl := template.Must(template.New("").Funcs(template.FuncMap{
				"import": func(v int) string { return versionImports[v] },
			}).Parse(string(af)))

			var b bytes.Buffer

			err = tpl.Execute(&b, map[string]interface{}{
				"versions":      versions,
				"latestVersion": latestVersion,
			})
			if err != nil {
				return err
			}

			if err := ioutil.WriteFile(filepath.Join(actDir, fmt.Sprintf("%s.go", act)), b.Bytes(), 0o666); err != nil {
				return err
			}
		}
	}

	return nil
}

func generateState(actDir string) error {
	af, err := ioutil.ReadFile(filepath.Join(actDir, "state.go.template"))
	if err != nil {
		if os.IsNotExist(err) {
			return nil // skip
		}

		return fmt.Errorf("loading state adapter template: %w", err)
	}

	for _, version := range versions {
		fmt.Println("parsing", actDir)
		tpl := template.Must(template.New("").Funcs(template.FuncMap{}).Parse(string(af)))

		var b bytes.Buffer

		err := tpl.Execute(&b, map[string]interface{}{
			"v":      version,
			"import": versionImports[version],
		})
		if err != nil {
			return err
		}

		if err := ioutil.WriteFile(filepath.Join(actDir, fmt.Sprintf("v%d.go", version)), b.Bytes(), 0o666); err != nil {
			return err
		}
	}

	return nil
}

